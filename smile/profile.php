                <p> <align="justify">Kehadiran kereta api di Indonesia ditandai dengan pencangkulan pertama pembangunan jalan KA di desa Kemijen, Jum'at tanggal 17 Juni 1864 oleh Gubernur Jenderal Hindia Belanda, Mr. L.A.J Baron Sloet van den Beele. Pembangunan diprakarsai oleh Naamlooze Venootschap Nederlandsch Indische Spoorweg Maatschappij (NV. NISM) yang dipimpin oleh Ir. J.P de Bordes dari Kemijen menuju desa Tanggung (26 Km) dengan lebar sepur 1435 mm. Ruas jalan ini dibuka untuk angkutan umum pada hari Sabtu, 10 Agustus 1867.

